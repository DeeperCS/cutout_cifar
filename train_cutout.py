def time_string():
  ISOTIMEFORMAT='%Y-%m-%d %X'
  string = '[{:}]'.format(time.strftime( ISOTIMEFORMAT, time.gmtime(time.time()) ))
  return string

def time_for_file():
  ISOTIMEFORMAT='%d-%h-at-%H-%M-%S'
